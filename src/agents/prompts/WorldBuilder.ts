import { getRegistry } from '../../genre-packs/index.js';
import { getDatabase } from '../../db/index.js';
import { queryOne } from '../../db/helpers.js';

function getGenrePackSection(): string {
  try {
    const db = getDatabase();
    if (!db) return '';
    const row = queryOne('SELECT genre_pack_id FROM projects LIMIT 1');
    if (!row || !row.genre_pack_id) return '';
    const registry = getRegistry();
    const pack = registry.get(row.genre_pack_id as string);
    if (!pack) return '';

    const rulesList = pack.writingRules.forbiddenPatterns
      .concat(pack.writingRules.recommendedPatterns.map(p => `推荐: ${p}`))
      .map(r => `- ${r}`)
      .join('\n');

    return `

## 当前题材约束：${pack.name}
- 力量体系：${pack.powerSystem.name}，等级：${pack.powerSystem.levels.join(' → ')}
- 角色类型：${pack.characterRoles.map(r => r.name).join('、')}
- 世界类型：${pack.worldTypes.join('、')}
- 写作规则：
${rulesList}`;
  } catch {
    return '';
  }
}

export function getWorldBuilderPrompt(): string {
  return WORLD_BUILDER_PROMPT + getGenrePackSection();
}

