import { getRegistry } from '../../genre-packs/index.js';
import { getDatabase } from '../../db/index.js';
import { queryOne } from '../../db/helpers.js';

function getGenrePackReviewSection(): string {
  try {
    const db = getDatabase();
    if (!db) return '';
    const row = queryOne('SELECT genre_pack_id FROM projects LIMIT 1');
    if (!row || !row.genre_pack_id) return '';
    const registry = getRegistry();
    const pack = registry.get(row.genre_pack_id as string);
    if (!pack) return '';

    const rulesChecklist = pack.writingRules.forbiddenPatterns
      .map(r => `- [ ] ${r}`)
      .join('\n');

    return `

## 当前题材审查标准：${pack.name}
- 力量体系一致性：检查角色能力是否符合${pack.powerSystem.name}的等级设定
- 世界观一致性：检查场景是否符合${pack.worldTypes}的类型
- 写作规则合规：
${rulesChecklist}`;
  } catch {
    return '';
  }
}

export function getReviewerPrompt(): string {
  return REVIEWER_PROMPT + getGenrePackReviewSection();
}

